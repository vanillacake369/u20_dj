$(document).on("click", "input[name='newrecord[]']", function () {
  console.log("click");
  const form = document.createElement("form");
  var url = "../action/record/change_record_category.php";
  form.setAttribute("method", "post");
  form.setAttribute("action", url);
  form.setAttribute("target", "popup test");
  var name = "popup test";
  var option =
    "width = 500, height = 500, top = 100, left = 200, location = no";
  window.open("", name, option);
  const hidden_record = document.createElement("input");
  hidden_record.setAttribute("type", "hidden");
  hidden_record.setAttribute("name", "gamerecord");
  hidden_record.setAttribute("value", $(this).attr("record"));
  form.appendChild(hidden_record);
  const hidden_name = document.createElement("input");
  hidden_name.setAttribute("type", "hidden");
  hidden_name.setAttribute("name", "athlete_name");
  hidden_name.setAttribute("value", $(this).attr("ath"));
  form.appendChild(hidden_name);
  const hidden_sports = document.createElement("input");
  hidden_sports.setAttribute("type", "hidden");
  hidden_sports.setAttribute("name", "sports");
  hidden_sports.setAttribute("value", $(this).attr("sports"));
  form.appendChild(hidden_sports);
  const hidden_gender = document.createElement("input");
  hidden_gender.setAttribute("type", "hidden");
  hidden_gender.setAttribute("name", "gender");
  hidden_gender.setAttribute("value", $(this).attr("record_gender"));
  form.appendChild(hidden_gender);
  const hidden_round = document.createElement("input");
  hidden_round.setAttribute("type", "hidden");
  hidden_round.setAttribute("name", "round");
  hidden_round.setAttribute("value", $(this).attr("record_round"));
  form.appendChild(hidden_round);
  const hidden_group = document.createElement("input");
  hidden_group.setAttribute("type", "hidden");
  hidden_group.setAttribute("name", "group");
  hidden_group.setAttribute("value", $(this).attr("record_group"));
  form.appendChild(hidden_group);
  document.body.appendChild(form);
  form.submit();
});
